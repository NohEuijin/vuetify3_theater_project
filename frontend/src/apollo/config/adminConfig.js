const config = {
  Sidebar_drawer: true,
  mini_sidebar: false,
  actTheme: 'DefaultTheme',
  fontTheme: 'Public sans'
};

export default config;

/*
해당 코드는 config라는 변수에 다양한 설정 값을 포함하는 객체를 정의

이 설정 값들은 아마도 웹 애플리케이션의 레이아웃 및 테마 등을 설정하는 데 사용

Sidebar_drawer: 사이드바가 화면에 표시되는지 여부를 나타내는 부울(boolean) 값입니다. 현재는 true로 설정되어 있으므로 사이드바가 표시됩니다.

mini_sidebar: 사이드바가 축소되어 있는지 여부를 나타내는 부울(boolean) 값입니다. 현재는 false로 설정되어 있으므로 사이드바가 축소되어 있지 않습니다.

actTheme: 애플리케이션의 활성 테마를 지정하는 문자열 값입니다. 현재는 'DefaultTheme'으로 설정되어 있습니다.

fontTheme
: 애플리케이션의 글꼴 테마를 지정하는 문자열 값입니다. 현재는 'Public sans'로 설정되어 있습니다.

마지막으로,
export default config;는 config 객체를 기본으로 내보냅니다.
이렇게 함으로써 이 객체는 이 파일을 가져와서 다른 파일에서 사용될 수 있습니다.
*/
